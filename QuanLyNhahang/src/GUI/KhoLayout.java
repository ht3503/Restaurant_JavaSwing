    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed


    //test
    int selectedRow = tblKho.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một mục trong kho!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Lấy mã nguyên liệu từ hàng được chọn
    int maNguyenLieu = (int) tblKho.getValueAt(selectedRow, 0);
    String tenNguyenLieu = (String) tblKho.getValueAt(selectedRow, 1);
    String donViTinh = (String) tblKho.getValueAt(selectedRow, 2);
    double soLuongTonHienTai = (double) tblKho.getValueAt(selectedRow, 3);

    // Hiển thị hộp thoại để nhập số lượng cần thêm
    String soLuongThemText = JOptionPane.showInputDialog(this, "Nhập số lượng cần thêm:");
    if (soLuongThemText == null || soLuongThemText.trim().isEmpty()) {
        return; // Người dùng nhấn Cancel hoặc không nhập gì
    }

    double soLuongThem;
    try {
        soLuongThem = Double.parseDouble(soLuongThemText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Số lượng không hợp lệ!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Cập nhật số lượng tồn
    double soLuongTonMoi = soLuongTonHienTai + soLuongThem;

    // Tạo đối tượng Kho và cập nhật số lượng tồn mới
    Kho kho = new Kho();
    kho.setMaNguyenLieu(maNguyenLieu);
    kho.setTenNguyenLieu(tenNguyenLieu);
    kho.setDonViTinh(donViTinh);
    kho.setSoLuongTon(soLuongTonMoi);

    // Thực hiện cập nhật vào cơ sở dữ liệu
    boolean thanhCong = KhoDao.capNhatKho(kho);
    if (thanhCong) {
        JOptionPane.showMessageDialog(this, "Cập nhật kho thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        loadTableData(); // Gọi phương thức loadTableData() để cập nhật bảng
    } else {
        JOptionPane.showMessageDialog(this, "Cập nhật kho thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
    
    }//GEN-LAST:event_btnThemActionPerformed


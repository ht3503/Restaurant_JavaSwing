    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
              // TODO add your handling code here:
    int selectedRow = tblKho.getSelectedRow();
    if (selectedRow != -1) {
        DefaultTableModel dtmMonAn = (DefaultTableModel) tblKho.getModel();

        // Lấy dữ liệu từ các trường văn bản
        int maNL = Integer.parseInt(txtMaNL.getText());
        String tenNguyenLieu = txtTenNguyenLieu.getText();
        String donViTinh = txtDonViTinh.getText();
        double soLuongTon = Double.parseDouble(txtSoLuongTon.getText());
        
        // Kiểm tra các trường có rỗng hay không
        if (tenNguyenLieu.isEmpty() || donViTinh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Lấy mã nguyên liệu từ hàng đã chọn
        int maNguyenLieu = Integer.parseInt(dtmMonAn.getValueAt(selectedRow, 0).toString());

        // Tạo đối tượng Kho và cập nhật các trường
        Kho kho = new Kho();
        kho.setMaNguyenLieu(maNguyenLieu); // Giữ nguyên mã nguyên liệu
        kho.setTenNguyenLieu(tenNguyenLieu);
        kho.setDonViTinh(donViTinh);
        kho.setSoLuongTon(soLuongTon);

        // Thực hiện cập nhật dữ liệu trong cơ sở dữ liệu
        boolean thanhCong = KhoDao.suaThongTinKho(kho);

        // Hiển thị thông báo kết quả
        if (thanhCong) {
            JOptionPane.showMessageDialog(this, "Updated successfully!");
            loadTableData(); // Tải lại dữ liệu của bảng
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a dish to edit", "No Selection", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnSuaActionPerformed


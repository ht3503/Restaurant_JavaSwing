    private void btbThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbThemMoiActionPerformed
      //   TODO add your handling code here:
   // Lấy dữ liệu từ các trường nhập liệu
    String maNLText = txtMaNL.getText().trim();
    String tenNguyenLieu = txtTenNguyenLieu.getText().trim(); 
    String donViTinh = txtDonViTinh.getText().trim();
    String soLuongTonText = txtSoLuongTon.getText().trim();
        
    // Kiểm tra xem các trường có rỗng không
    if (tenNguyenLieu.isEmpty() || donViTinh.isEmpty() || soLuongTonText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }
    double soLuongTon;
    try {
        soLuongTon = Double.parseDouble(soLuongTonText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Số lượng tồn không hợp lệ!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Kiểm tra xem tên nguyên liệu đã tồn tại trong bảng hay chưa
    ArrayList<Kho> danhSachKho = KhoDao.LayDanhSachKho();
    for (Kho kho : danhSachKho) {
        if (kho.getTenNguyenLieu().equalsIgnoreCase(tenNguyenLieu)) {
            JOptionPane.showMessageDialog(this, "Tên nguyên liệu đã tồn tại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }

    // Tạo đối tượng Kho
    Kho kho = new Kho();
    kho.setTenNguyenLieu(tenNguyenLieu);
    kho.setDonViTinh(donViTinh);
    kho.setSoLuongTon(soLuongTon);

    // Thực hiện thêm vào cơ sở dữ liệu
    boolean thanhCong = KhoDao.ThemKho(kho);
    // Hiển thị thông báo kết quả
    if (thanhCong) {
        JOptionPane.showMessageDialog(this, "Thêm món thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        loadTableData(); // Gọi phương thức loadTableData() để cập nhật bảng
    } else {
        JOptionPane.showMessageDialog(this, "Thêm món thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btbThemMoiActionPerformed

    private void btnXuatKhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatKhoActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblKho.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nguyên liệu cần xuất!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int maNguyenLieu = Integer.parseInt(tblKho.getValueAt(selectedRow, 0).toString());
    String tenNguyenLieu = tblKho.getValueAt(selectedRow, 1).toString();

    String soLuongXuatText = JOptionPane.showInputDialog(this, "Nhập số lượng cần xuất:");
    if (soLuongXuatText == null || soLuongXuatText.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Số lượng xuất không hợp lệ!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    double soLuongXuat;
    try {
        soLuongXuat = Double.parseDouble(soLuongXuatText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Số lượng xuất không hợp lệ!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        return;
    }

    boolean thanhCong = KhoDao.XuatKho(maNguyenLieu, soLuongXuat);
    if (thanhCong) {
        JOptionPane.showMessageDialog(this, "Xuất kho thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        loadTableData(); // Cập nhật lại bảng kho
    } else {
        Kho kho = KhoDao.layKhoTheoMa(maNguyenLieu);
        if (kho != null && soLuongXuat > kho.getSoLuongTon()) {
            JOptionPane.showMessageDialog(this, "Số lượng xuất lớn hơn số lượng tồn!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Xuất kho thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnXuatKhoActionPerformed

